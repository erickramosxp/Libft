#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ch)
			return ((char *)&str[i]);
		i++;
	}
	if (str[i] == ch)
		return ((char *)&str[i]);
	return (NULL);
}

#include <stdio.h>

int	main(void)
{
	const char	*a = "      split       this for   me  !     ";
	
	printf("%s", ft_strchr(a, ' '));
}
