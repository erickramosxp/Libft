#include "libft.h"

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
		return (1);
	else
		return (0);
}

#include <stdio.h>

int	main(void)
{
	printf("%d", ft_isalnum(4));
}
